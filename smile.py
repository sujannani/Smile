import turtle
t = turtle.Turtle()
t.speed(5)
turtle.hideturtle()
turtle.bgcolor('black')
t.pencolor("yellow")
t.color("yellow")
t.begin_fill()
t.circle(100)
t.end_fill()
t.penup()
t.goto(35,110)
t.color("red")
t.begin_fill()
t.circle(15)
t.end_fill()
t.penup()
t.goto(-35,110)
t.color("red")
t.begin_fill()
t.circle(15)
t.end_fill()
t.penup()
t.goto(0,45)
t.pendown()
t.pensize(9)
t.circle(80,40)
t.circle(80,-80)
t.hideturtle()
turtle.done()